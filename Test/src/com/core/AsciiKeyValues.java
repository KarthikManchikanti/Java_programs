//To get Ascii values 


public class AsciiKeyValues {

	public static void main(String[] args) {
		
		char c ='A';
		System.out.println((int)c);  //ascii value for A = 65
		
		char d=':';
		System.out.println((int)d);  //value for ":"= 58
		
		char a= 'a'; 
		System.out.println((int)a);   //value for a=97
		
		char s=' ';
		System.out.println((int)s);   // value for space=32
		
		
		//Reverse it give the value and get the ascii 
		
		
		System.out.println((char)99); //ascii code for 99 is c
		
		System.out.println((char)100);  //ascii code for 100 is d
		
	
		
		
		
		
		
		

	}

	
}


